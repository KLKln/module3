#capitalize
movie_character = ('Aldous Snow in Forgetting Sarah Marshall')
print(movie_character.capitalize())

#find
result = movie_character.find('in')
print(result)

#index
result = movie_character.index('Forgetting')
print(result)

#isalnum
result = movie_character.isalnum()
print(result)

#isalpha
result = movie_character.isalpha()
print(result)

#isdigit
result = movie_character.isdigit()
print(result)

#islower
result = movie_character.islower()
print(result)

#isupper
result = movie_character.isupper()
print(result)

#isspace
result = movie_character.isspace()
print(result)

#startswith
result = movie_character.startswith('Aldous')
print(result)
